<h2>Par le RER :</h2>
<p>
  RER Ligne A station "Noisy-Champs".<br/>
  Descente en tête de train depuis Paris, en queue de train sinon.<br/>
  Rechercher l'enseigne rouge de l'hôtel IBIS :
  C'est la direction que vous devez suivre.<br/>
  Au croisement précédant l'hôtel, tournez à droite : ESIEE Paris est situé à votre
  droite, à 2 minutes de marche en tout depuis la station de RER de Champs.
</p>
<h2>Par l'autoroute :</h2>
<p>
  Si vous souhaitez venir depuis Paris par la route, prenez l'autoroute A4,
  depuis le Boulevard Périphérique à la Porte de Bercy et suivez la
  direction Metz-Nancy-Marne-la-Vallée. Après 15 kilomètres, prenez la
  sortie "Champs". A la sortie de la bretelle prendre à gauche du
  rond-point, puis à droite au second rond-point et suivez la signalisation
  ESIEE-Cité Descartes. Vous reconnaîtrez ESIEE Paris grâce à l'aiguille
  géante qui se dresse devant le bâtiment.
</p>
